    private void btn_enviar_chatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviar_chatActionPerformed

        if(txt_escrever.getText().equals("Escrever") || txt_escrever.getText().equals(" ") ){
            txt_escrever.setText("Escrever");
        }else{
            String mensagem = txt_escrever.getText();
            client.sendMessage(mensagem);
            txt_area_chat_box.append(myName+": "+mensagem+"\n");
            txt_escrever.setText("Escrever");
            txt_area_chat_box.setRows(rows++);
        }
    }//GEN-LAST:event_btn_enviar_chatActionPerformed

