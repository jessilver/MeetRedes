    private void bnt_enviar_nomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnt_enviar_nomeActionPerformed
        myName = txt_nome.getText();
        porta = Integer.parseInt(txt_porta.getText());
        
        client = new Client("localhost", porta,myName);
        client.sendName(myName);

        pn_login.setVisible(false);
        pn_chat.setVisible(true);
        rows = 1;
        txt_area_client_list.append(myName);

        new Thread(new Runnable() { // espera uma mensgem do servidor
            public void run() {
                while (true) {
                    String message = client.receiveMessage();
                    if (message != null) {
                        SwingUtilities.invokeLater(new Runnable() {
                            public void run() {
                                txt_area_chat_box.append(message + "\n");
                            }
                        });
                    }
                }
            }
        }).start();
    }//GEN-LAST:event_bnt_enviar_nomeActionPerformed

