    public void waitFiveSeconds() {
        Timer timer = new Timer();
        TimerTask task = new TimerTask() {
            public void run() {
                // C√≥digo a ser executado ap√≥s 5 segundos
                System.out.println("5 segundos se passaram.");
            }
        };
        timer.schedule(task, 5000); // Agendar a tarefa para ser executada ap√≥s 5000 milissegundos (5 segundos)
    }

